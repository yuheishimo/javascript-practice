//typeofは値の型を判定
let num = 1;
console.log(typeof num);
//number

let str = 'こんにちは';
console.log(typeof str);
//string

let flag = true;
console.log(typeof flag);
//boolean

let ary = [ 'JavaScript', 'PHP', 'Python' ];
console.log(typeof ary);
//object

let obj = { x:1, y:2 };
console.log(typeof obj);
//object

let dat = new Date();
console.log(typeof dat);
//object